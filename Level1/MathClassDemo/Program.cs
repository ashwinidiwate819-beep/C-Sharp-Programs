using System;

class MathClassDemo
{
    static void Main()
    {
        Console.WriteLine("Square Root of 16: " + Math.Sqrt(16));
        Console.WriteLine("Power 2^3: " + Math.Pow(2, 3));
        Console.WriteLine("Maximum of 10 and 20: " + Math.Max(10, 20));
        Console.WriteLine("Minimum of 10 and 20: " + Math.Min(10, 20));
    }
}
